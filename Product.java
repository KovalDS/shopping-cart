package com.example.zoolatech;

public class Product {

    private long id;
    private String description;
    private double price;
    private double weight;

    public long getId() {
        return id;
    }

    public String getDescription() {
        return description;
    }

    public double getPrice() {
        return price;
    }

    public double getWeight() {
        return weight;
    }
}
